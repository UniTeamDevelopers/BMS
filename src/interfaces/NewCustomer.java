    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        int id = Integer.parseInt(txtID.getText().trim());
        String FName = txtF_name.getText().trim();
        String LName = txtL_name.getText().trim();
        int mobilePho = Integer.parseInt(txtMobile.getText().trim());
        
        try {
            // TODO add your handling code here:
            connectionClass management = new connectionClass();//connection object for NewCustomer
            try {
                management.connect();
            } catch (Exception e) {
                e.printStackTrace();
            }
            management.insertCustomer(id,FName,LName,mobilePho);
            management.close();
        } catch (SQLException ex) {
            Logger.getLogger(NewCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

